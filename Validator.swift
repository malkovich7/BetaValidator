public class Validator {
    
    public static func isUpper(numA: Int, numB: Int) -> Bool {
        return numA > numB
    }
    
    public static func isLower(numA: Int, numB: Int) -> Bool {
        return numA < numB
    }
    
    public static func isEqual(numA: Int, numB: Int) -> Bool {
        return numA == numB
    }
}
